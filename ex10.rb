# \t indents
tabby_cat = "\tI'm tabbed in."

# \n "linefeed" returns
persian_cat = "I'm split\non a line."
# \\ puts one backslash 
backslash_cat = "I'm \\ a \\ cat."

# """ at the top and bottom puts all of it in a list or paragraph
# \t indents/tabs
fat_cat = """
I'll do a list:
\t* Cat food
\t* Fishies
\t* Catnip\n\t* Grass
"""

puts tabby_cat
puts persian_cat
puts backslash_cat
puts fat_cat